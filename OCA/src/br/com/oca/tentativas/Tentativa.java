package br.com.oca.tentativas;

public class Tentativa {
	private Integer numeroTentativa;
	private Integer testeEscolhido;
	private Double nota;
	
	public Integer getNumeroTentativa() {
		return numeroTentativa;
	}
	public void setNumeroTentativa(Integer numeroTentativa) {
		this.numeroTentativa = numeroTentativa;
	}
	public Integer getTesteEscolhido() {
		return testeEscolhido;
	}
	public void setTesteEscolhido(Integer testeEscolhido) {
		this.testeEscolhido = testeEscolhido;
	}
	public Double getNota() {
		return nota;
	}
	public void setNota(Double nota) {
		this.nota = nota;
	}
	
	
}
